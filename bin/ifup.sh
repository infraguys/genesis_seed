set -eu

# Configure DNS resolvers
# Uses udhcpc-provided environment variables:
#  - $dns: space-separated list of DNS servers (option 6)
if [ -n "${dns-}" ]; then
    echo "DNS servers: $dns"
    # Create file with header if it doesn't exist. This prevents interfaces
    # from overwriting each other's DNS settings.
    if ! [ -f /etc/resolv.conf ]; then
        echo "# Generated by ifup.sh on $(date -u +'%Y-%m-%d %H:%M:%S UTC')" > /etc/resolv.conf
    fi
    for ns in $dns; do
        # Add nameserver if not already present to avoid duplicates.
        if ! grep -Fxq "nameserver $ns" /etc/resolv.conf; then
            echo "nameserver $ns" >> /etc/resolv.conf
        fi
    done
fi

echo "Bringing up $interface with IP $ip/${subnet?subnet mask not provided by DHCP}"
ifconfig "$interface" "$ip" netmask "$subnet" up

# Configure routes
if [ -n "$router" ] && ! ip route list | grep -q '^default'; then
    # Use only the first router from the list provided by DHCP.
    set -- $router
    echo "Adding default route via $1"
    ip route add default via "$1" dev "$interface"
fi
